 define([
    'uiComponent',
    'Magento_Customer/js/customer-data'
], function (Component, customerData) {
    'use strict';

    return Component.extend({
        initialize: function () {
            this._super();
        },

        getValue: function () {
            var rewardPoints = customerData.get('cart')().reward_earn_points;
            return rewardPoints;
        },
    });
});
